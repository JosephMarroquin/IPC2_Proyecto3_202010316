class Entrada:
    def __init__(self,fecha,total):
        self.fecha=fecha
        self.total=total

    def getFecha(self):
        return self.fecha
    
    def getTotal(self):
        return self.total

    def setFecha(self,fecha):
        self.fecha=fecha
    
    def setTotal(self,total):
        self.total=total
    